  CREATE OR REPLACE EDITIONABLE PROCEDURE "SALARY_USER"."GENERATE_SALARY" (p_EID NUMBER) IS
    v_SID NUMBER;
    v_Basic NUMBER;
    v_Allowance NUMBER;
    v_LeaveDays NUMBER := 0;
    v_Amount NUMBER;
BEGIN
    -- Get Employee's salary grade
    SELECT SID, Basic, Allowance 
    INTO v_SID, v_Basic, v_Allowance
    FROM Employee_Salary es
    JOIN Salary s ON es.SID = s.SID
    WHERE es.EID = p_EID;

    -- Get Leave days for current month, default 0 if not found
    BEGIN
        SELECT Leave_Days
        INTO v_LeaveDays
        FROM Leave_Record
        WHERE EID = p_EID
          AND TRIM(Month) = TO_CHAR(SYSDATE,'FMMonth');
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            v_LeaveDays := 0;
    END;

    -- Salary calculation (1000 per leave day deduction)
    v_Amount := v_Basic + v_Allowance - (v_LeaveDays * 1000);

    -- Insert transaction
    INSERT INTO Transaction_Record (EID, Salary_Amount)
    VALUES (p_EID, v_Amount);

    -- Deduct from fund
    UPDATE Fund SET Amount = Amount - v_Amount WHERE Fund_ID = 1;

    COMMIT;
END;
